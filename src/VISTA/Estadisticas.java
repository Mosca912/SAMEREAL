    public class PanelDibujo extends JPanel implements MouseMotionListener {

        private final List<Point> puntos = new ArrayList<>();

        public PanelDibujo() {
            this.addMouseMotionListener(this);
            this.setOpaque(true);                    // 游댠 Fondo opaco = real
            this.setBackground(Color.WHITE);         // 游댠 Fondo blanco

            // 游꿛 Cargar imagen del cursor personalizado (debe ser 32x32 o menos)
            Toolkit toolkit = Toolkit.getDefaultToolkit();
            Image cursorImg = toolkit.getImage(getClass().getResource("/IMAGENES/lapiz.png"));

            // 游늸 Crear cursor
            Point hotspot = new Point(0, 0); // Donde est치 la punta del l치piz
            Cursor cursorPersonalizado = toolkit.createCustomCursor(cursorImg, hotspot, "Lapiz");

            // 游대 Aplicar cursor al panel
            this.setCursor(cursorPersonalizado);
        }

        @Override
        protected void paintComponent(Graphics g) {
            super.paintComponent(g);                 // 游댠 Limpia lo anterior
            g.setColor(Color.BLACK);                 // 游댠 Color de dibujo
            for (Point p : puntos) {
                g.fillOval(p.x, p.y, 4, 4);   // 游댠 Dibujo real
            }
        }

        @Override
        public void mouseDragged(MouseEvent e) {
            puntos.add(e.getPoint());
            repaint();
        }

        @Override
        public void mouseMoved(MouseEvent e) {
            // No se usa en este caso
        }
    }

