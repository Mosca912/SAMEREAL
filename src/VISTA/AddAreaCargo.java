    private void accionfinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accionfinActionPerformed
        String nomb = text1.getText();
        if (ban == 1) {
            if (!nomb.trim().isEmpty() && !veri.equals("Opciones")) {
                try {
                    CLASES.Empleados.AgregarCargo(con, nomb, id);
                    JOptionPane.showMessageDialog(null, "Cargao");
                    this.dispose();
                } catch (SQLException ex) {
                    Logger.getLogger(AddAreaCargo.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "¡HAY CAMPOS VACIOS! Por favor, revise");
            }
        } else if (ban == 0) {
            if (!nomb.trim().isEmpty()) {
                try {
                    CLASES.Empleados.AgregarArea(con, nomb, id);
                    JOptionPane.showMessageDialog(null, "Cargado");
                    this.dispose();
                } catch (SQLException ex) {
                    Logger.getLogger(AddAreaCargo.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "¡HAY CAMPOS VACIOS! Por favor, revise");
            }
        }

    }//GEN-LAST:event_accionfinActionPerformed

